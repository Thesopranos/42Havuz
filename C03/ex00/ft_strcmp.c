int	ft_strcmp(char *s1, char *s2)
{
	int	ctr;

	ctr = 0;
	while (s1[ctr] == s2[ctr] && (s1[ctr] != '\0' || s2[ctr] != '\0'))
	{	
		ctr ++;
	}
	return (s1[ctr] - s2[ctr]);
}
